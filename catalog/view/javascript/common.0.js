function getURLVar(key) {
	var value = [];

	var query = String(document.location).split('?');

	if (query[1]) {
		var part = query[1].split('&');

		for (i = 0; i < part.length; i++) {
			var data = part[i].split('=');

			if (data[0] && data[1]) {
				value[data[0]] = data[1];
			}
		}

		if (value[key]) {
			return value[key];
		} else {
			return '';
		}
	}
}

$(document).ready(function() {
	/* Search */
	$('.search-block .input-field label i.btn-search').on('click', function() {
		url = $('base').attr('href') + 'index.php?route=product/search';

		var value = $('.search-block .input-field input[type=\'search\']').val();
		if (value) {
			url += '&search=' + encodeURIComponent(value);
		}

		location = url;
	});

	$('.search-block .input-field input[type=\'search\']').on('keydown', function(e) {
		if (e.keyCode == 13) {
			$('.search-block .input-field label i.btn-search').trigger('click');
		}
	});

	$('.search-block .input-field i.btn-clear').on('click', function() {
		url = $('base').attr('href') + 'index.php?route=product/search';

		$('.search-block .input-field input[type=\'search\']').val('');
	});

});

